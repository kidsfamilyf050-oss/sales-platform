// ── Store ────────────────────────────────────────────────────────────────────

const translations: Record<Lang, typeof ru> = { ru, kk }

interface I18nStore {
  lang: Lang
  setLang: (lang: Lang) => void
  t: (key: keyof typeof ru, vars?: Record<string, string | number>) => string
}

const saved = (typeof localStorage !== 'undefined' ? localStorage.getItem('lang') : null) as Lang | null

export const useI18n = create<I18nStore>((set, get) => ({
  lang: saved || 'ru',
  setLang: (lang) => {
    localStorage.setItem('lang', lang)
    set({ lang })
  },
  t: (key, vars) => {
    const { lang } = get()
    let str = translations[lang][key] ?? translations['ru'][key] ?? key
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        str = str.replace(`{{${k}}}`, String(v))
      })
    }
    return str
  },
}))

// Convenience hook — just returns t and lang
export function useT() {
  const { t, lang, setLang } = useI18n()
  return { t, lang, setLang }
}

